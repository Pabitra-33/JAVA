package com.stringmethods;

public class LengthMethod {
	public static void main(String[] args) {
		//creating string by using String literal
		String str = "Java Is a Very Easy Language, Love It!";
		
		//string has one of it's method to calculate it's length or no of letters present in the string
		int length = str.length();//by using length method we can get the length of the string
		System.out.println("Length of String is: "+length); //38
	}
}