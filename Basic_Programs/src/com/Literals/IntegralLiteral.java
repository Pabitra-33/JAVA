package com.Literals;

public class IntegralLiteral {
	public static void main(String[] args) {
		int a = 10; //integer value
		int b = 010; //octal value
		int c = 0x10; //hexa-decimal value
		
		//print the values
		System.out.println("Value of a= "+a);//10
		System.out.println("Value of b= "+b);//8
		System.out.println("Value of c= "+c);//16
	}
}
